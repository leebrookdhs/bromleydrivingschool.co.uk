                            <h2 class="fw-bold  mb-3">The key things for newly passed drivers to do</h2>
                            <p class="mb-4">As a new driver, one of the hardest things to deal with is the number of
                                hazards on the road. With more vehicles, pedestrians, cyclist and other
                                distractions, you need to be very aware as you drive. So what principles can you
                                remember to help you be as safe as possible on the road and be aware of those
                                hazards?</p>
                            <h3>Focus on the road</h3>

                            <p class="mb-4">As well as being many hazards outside the car, there can also be many
                                distractions inside the car but when you are driving you need to focus all of your
                                attention on the road. For starters, it is illegal to use a hand-held mobile phone
                                or other devices while driving unless it is to call 999 if there is an emergency. If
                                you need to use the phone, simply pull over somewhere safe.</p>

                            <p class="mb-4">In the same way, things like messing around with the car stereo, heating
                                system or any other non-essential systems can take your attention away from the
                                road. Set the car up before you leave and try to avoid messing around with these
                                things while driving. Never try to pick up anything you have dropped on the floor
                                while driving either.</p>

                            <h3>Be calm and alert</h3>

                            <p class="mb-4">Aggression on the roads is a sadly common part of modern society but it
                                is important to do as much as you can to avoid this yourself. Don't drive if you are
                                angry or upset about something and should you encounter someone who is aggressive
                                and confrontational, try not to engage them, especially if you are still driving.
                            </p>

                            <p class="mb-4">One of the biggest causes of accidents can be people driving while tired
                                and losing their concentration or even falling asleep at the wheel. If you are
                                tired, don't drive or if you are on a long journey and feel yourself becoming tired,
                                then find somewhere to stop and take a short break.</p>

                            <h3>Expect the unexpected</h3>

                            <p class="mb-4">While you might know what drivers are meant to do in a certain
                                situation, this doesn't always mean they actually will so it pays to drive somewhat
                                defensively. This gives you time to react to anything unexpected or random that
                                other drivers do before an accident can occur.</p>

                            <p class="mb-4">Always leave a safe distance between yours and another vehicle. A simple
                                trick to measure this is to pick a landmark and when the vehicle in front passes it,
                                count for two seconds and if pass it before this mark, then drop back a bit. If the
                                weather is bad or you are towing a trailer or caravan, then leave a bigger gap as
                                the car will need more space to stop.</p>

                            <h3>Plan your route</h3>

                            <p class="mb-4">While satellite navigation systems are brilliant, it never hurts to plan
                                your route somewhat, especially if you are going somewhere for the first time. Check
                                the internet for roadworks and look for stopping places if you are going on a longer
                                journey. Always leave extra time so you don't feel rushed and have the urge to speed
                                to make sure you arrive on time. And always check all the main systems on the car
                                before leaving.</p>
                            <p class="mb-4">For most people turning seventeen means one thing; learning to drive.
                                For not for everyone and there are many people who, for one reason or another, do
                                not feel that urge. They hit their twenties and still never get around to learning
                                to drive. Then they reach their thirties and the irritation with public transport
                                reaches fever pitch or the person they relied on to drive for them cannot do it
                                anymore. So they start looking for a driving instructor. But is it harder to learn
                                to drive in your thirties?</p>

